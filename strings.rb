kalimat = "ini adalah string"

# menghitung jumlah teks

puts kalimat.size # 17

# merubah teks menjadi kapital

puts kalimat.upcase # INI ADALAH STRING

# merubah string ke integer

puts kalimat.to_i # 0

# merubah string ke string

puts kalimat.to_s # ini adalah string

# merubah string ke char

puts kalimat.chars  # [i,n,i a,d,a,l,a,h s,t,r,i,n,g]
